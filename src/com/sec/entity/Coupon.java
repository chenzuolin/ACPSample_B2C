package com.sec.entity;

public class Coupon {
	private int Coupon_ID;
	private int Coupon_Point;
	private int Coupon_num;
	private String Coupon_Type;
	private String Coupon_Mode;
	private String Coupon_Tupian;
	public int getCoupon_ID() {
		return Coupon_ID;
	}
	public void setCoupon_ID(int coupon_ID) {
		Coupon_ID = coupon_ID;
	}
	public int getCoupon_Point() {
		return Coupon_Point;
	}
	public void setCoupon_Point(int coupon_Point) {
		Coupon_Point = coupon_Point;
	}
	public int getCoupon_num() {
		return Coupon_num;
	}
	public void setCoupon_num(int coupon_num) {
		Coupon_num = coupon_num;
	}
	public String getCoupon_Type() {
		return Coupon_Type;
	}
	public void setCoupon_Type(String coupon_Type) {
		Coupon_Type = coupon_Type;
	}
	public String getCoupon_Mode() {
		return Coupon_Mode;
	}
	public void setCoupon_Mode(String coupon_Mode) {
		Coupon_Mode = coupon_Mode;
	}
	public String getCoupon_Tupian() {
		return Coupon_Tupian;
	}
	public void setCoupon_Tupian(String coupon_Tupian) {
		Coupon_Tupian = coupon_Tupian;
	}

}
