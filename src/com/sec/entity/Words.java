package com.sec.entity;

public class Words {
	private int words_id;
	private int wineshop_id;
	private String words_title;
	private String words_time;
	private String words_type;
	private String words_content;
	public int getWords_id() {
		return words_id;
	}
	public void setWords_id(int words_id) {
		this.words_id = words_id;
	}
	public int getWineshop_id() {
		return wineshop_id;
	}
	public void setWineshop_id(int wineshop_id) {
		this.wineshop_id = wineshop_id;
	}
	public String getWords_title() {
		return words_title;
	}
	public void setWords_title(String words_title) {
		this.words_title = words_title;
	}
	public String getWords_time() {
		return words_time;
	}
	public void setWords_time(String words_time) {
		this.words_time = words_time;
	}
	public String getWords_type() {
		return words_type;
	}
	public void setWords_type(String words_type) {
		this.words_type = words_type;
	}
	public String getWords_content() {
		return words_content;
	}
	public void setWords_content(String words_content) {
		this.words_content = words_content;
	}
}
