package com.sumeng.service;

public class PareMenu {
	private String pare_menu_name;
	private String pare_menu_path;
	private int starts;
	private int code;
	public String getPare_menu_name() {
		return pare_menu_name;
	}
	public void setPare_menu_name(String pare_menu_name) {
		this.pare_menu_name = pare_menu_name;
	}
	public String getPare_menu_path() {
		return pare_menu_path;
	}
	public void setPare_menu_path(String pare_menu_path) {
		this.pare_menu_path = pare_menu_path;
	}
	public int getStarts() {
		return starts;
	}
	public void setStarts(int starts) {
		this.starts = starts;
	}
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}

}
